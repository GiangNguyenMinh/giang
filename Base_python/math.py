# khai báo thư viện math 

import math 

x= 1.4 

# trunc() trả về phần nguyển của 
print(math.trunc(x))
#floor() trả về giá trị làm tròn về nhỏ hơn hoặc bằng x
print(math.floor(x))
#ceil() trả về giá trị làm tròn về lớn hơn hoặc bắng x
print(math.ceil(x))
#fabs() trả về 1 số thực là giá trị tuyệt đối của x
print(math.fabs(x))
#sqrt() trả về cắn bậc 2 của x
print(math.sqrt(x))


c = 3 
